  def addTags(id: UUID, userIdStr: Option[String], eid: Option[String], tags: List[String]) : List[Tag] = {
    Logger.debug("Adding tags to file " + id + " : " + tags)

    var tagsAdded : ListBuffer[Tag] = ListBuffer.empty[Tag]
